// funcao que define o turno do jogador | function that defines the player's turn
int turno (int x) {
	int y, rodada=1, valordado, posicaoanterior;

	while (fim==0) {
	
		for (int vez=0;vez<qtd;vez++){
			cor (jogador[vez].personagem);
			
			cabecalho (vez, rodada);
	
			movimento (y);
	
			tabuleiro (y);
			
			if (jogador[vez].posicao < 32) {
						
				valordado = dado (y);
				
				tabul[jogador[vez].posicao][vez+1] = 0;
				
				jogador[vez].posicao = jogador[vez].posicao + valordado;
				
				if (jogador[vez].posicao > 31) {
					jogador[vez].posicao = 32;
				}
				
				cabecalho (vez, rodada);
				
				movimento (y);
		
				tabuleiro (y);
			}
			
			printf ("\n\n Obter Evento:\n [1] Sim \n Resposta: ");
			scanf ("%d", &y);
			y = 0;
			system ("cls");
			
			cabecalho (vez, rodada);
			
			extra = perguntas (jogador[vez].personagem, jogador[vez].posicao);
			
			if (extra == 4) {
				extra = 0;
				fim = 1;
			}
			
			tabul[jogador[vez].posicao][vez+1] = 0;
			
			jogador[vez].posicao = jogador[vez].posicao + extra;
			
			if (jogador[vez].posicao > 31) {
				jogador[vez].posicao = 32;
			}
			
			cabecalho (vez, rodada);
			
			movimento (y);
	
			tabuleiro (y);
			
			printf ("\n\n ----------\n ");
			system ("pause");
			system ("cls");
			
			if (fim == 1) {
				break;
			}
			
		}
		rodada ++;
	}
	return 0;
}

