// funcao para gerar o valor que sera impresso caso mais de um jogador esteja na mesma posicao | function to generate the value that will be printed if more than one player is in the same position
int peca (int a, int b, int c, int d, int e) {
	int f = 0;
	
	if (b == 0)
		f ++;
	if (c == 0)
		f ++;
	if (d == 0)
		f ++;
	if (e == 0)
		f ++;
	
	switch (f) {
		case 0:
			return 52;
			break;
		case 1:
			return 51;
			break;
		case 2:
			return 50;
			break;
		case 3:
			if (b != 0)
				return b;
			if (c != 0)
				return c;
			if (d != 0)
				return d;
			if (e != 0)
				return e;
			break;
		case 4:
			return a;
			break;
	}	
}

